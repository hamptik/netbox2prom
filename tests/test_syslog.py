class TestRunCtl:
    def test_successful_command(self) -> None:
        mock_result = MagicMock()
        mock_result.returncode = 0
        mock_result.stdout = "ok\n"
        mock_result.stderr = ""

        with patch("netbox2prom.generators.syslog.subprocess.run", return_value=mock_result):
            assert _run_ctl(["echo", "hi"]) == "ok"

    def test_failed_command_returns_none(self) -> None:
        mock_result = MagicMock()
        mock_result.returncode = 1
        mock_result.stdout = ""
        mock_result.stderr = "error"

        with patch("netbox2prom.generators.syslog.subprocess.run", return_value=mock_result):
            assert _run_ctl(["bad"]) is None

    def test_timeout_returns_none(self) -> None:
        with patch(
            "netbox2prom.generators.syslog.subprocess.run",
            side_effect=subprocess.TimeoutExpired(cmd="x", timeout=15),
        ):
            assert _run_ctl(["slow"]) is None

    def test_filenotfound_returns_none(self) -> None:
        with patch(
            "netbox2prom.generators.syslog.subprocess.run",
            side_effect=FileNotFoundError("not found"),
        ):
            assert _run_ctl(["missing"]) is None


