class TestBuildContent:
    def test_basic_content(self) -> None:
        content = _build_content("fix_hostnames", ["rule1;", "rule2;"], "")
        assert content.startswith("# Generated by netbox2prom")
        assert "rewrite fix_hostnames {" in content
        assert "rule1;" in content
        assert "rule2;" in content
        assert content.rstrip().endswith("};")

    def test_with_epilogue(self) -> None:
        content = _build_content("test_block", ["rule1;"], "extra_config;")
        assert "extra_config;" in content
        assert content.rstrip().endswith("};")

    def test_empty_rules(self) -> None:
        content = _build_content("test_block", [], "")
        assert "rewrite test_block {" in content
        assert content.rstrip().endswith("};")


