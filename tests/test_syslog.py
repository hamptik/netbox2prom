class TestGenerateSyslogConfig:
    def test_no_config_file_returns_false(self) -> None:
        result = generate_syslog_config([], {"groups": {}})
        assert result is False

    def test_no_matching_devices_no_existing_file(self, tmp_path) -> None:
        cfg = {
            "config_file": str(tmp_path / "syslog.conf"),
            "block_name": "fix_hostnames",
            "syntax_check": False,
            "groups": {
                "g": {"conditions": {"role": "router"}, "template": "rule;"},
            },
        }
        result = generate_syslog_config([make_device(role="switch")], cfg)
        assert result is False
        assert not (tmp_path / "syslog.conf").exists()

    def test_no_matching_devices_removes_existing_file(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        config_file.write_text("old content")

        cfg = {
            "config_file": str(config_file),
            "block_name": "fix_hostnames",
            "syntax_check": False,
            "groups": {
                "g": {"conditions": {"role": "router"}, "template": "rule;"},
            },
        }
        result = generate_syslog_config([make_device(role="switch")], cfg)
        assert result is True
        assert not config_file.exists()

    def test_writes_new_config(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        cfg = {
            "config_file": str(config_file),
            "block_name": "fix_hostnames",
            "syntax_check": False,
            "groups": {
                "g": {
                    "conditions": {},
                    "host_field": "main_ip",
                    "template": 'set("{name}" value(".Host") source-ip("{ip}"));',
                },
            },
        }
        devices = [
            make_device(name="rtr1", main_ip="10.0.0.1"),
            make_device(name="rtr2", main_ip="10.0.0.2"),
        ]
        result = generate_syslog_config(devices, cfg)

        assert result is True
        content = config_file.read_text()
        assert "rtr1" in content
        assert "10.0.0.1" in content
        assert "rtr2" in content

    def test_unchanged_config_skips_write(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        cfg = {
            "config_file": str(config_file),
            "block_name": "fix_hostnames",
            "syntax_check": False,
            "groups": {
                "g": {
                    "conditions": {},
                    "host_field": "main_ip",
                    "template": 'set("{name}" value(".Host"));',
                },
            },
        }
        devices = [make_device(name="rtr1", main_ip="10.0.0.1")]

        # First write
        generate_syslog_config(devices, cfg)
        first_mtime = config_file.stat().st_mtime

        # Second run with same data
        import time

        time.sleep(0.05)
        result = generate_syslog_config(devices, cfg)

        assert result is False
        assert config_file.stat().st_mtime == first_mtime

    def test_skips_devices_without_ip(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        cfg = {
            "config_file": str(config_file),
            "block_name": "fix_hostnames",
            "syntax_check": False,
            "groups": {
                "g": {
                    "conditions": {},
                    "host_field": "main_ip",
                    "template": 'set("{name}" value(".Host"));',
                },
            },
        }
        devices = [make_device(name="rtr1", main_ip=None)]
        result = generate_syslog_config(devices, cfg)

        assert result is False
        assert not config_file.exists()

    def test_skips_devices_without_name(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        cfg = {
            "config_file": str(config_file),
            "syntax_check": False,
            "groups": {
                "g": {
                    "conditions": {},
                    "host_field": "main_ip",
                    "template": 'set("{name}" value(".Host"));',
                },
            },
        }
        devices = [make_device(name=None, main_ip="10.0.0.1")]
        result = generate_syslog_config(devices, cfg)
        assert result is False

    def test_name_quote_escaping(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"
        cfg = {
            "config_file": str(config_file),
            "syntax_check": False,
            "groups": {
                "g": {
                    "conditions": {},
                    "host_field": "main_ip",
                    "template": 'set("{name}" value(".Host"));',
                },
            },
        }
        devices = [make_device(name='device"in"quotes', main_ip="10.0.0.1")]
