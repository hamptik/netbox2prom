class TestReloadSyslog:
    def test_no_ctl_binary_skips_gracefully(self) -> None:
        with patch("netbox2prom.generators.syslog.shutil.which", return_value=None):
            reload_syslog({"config_file": "/tmp/test.conf"})
            # No exception raised is the assertion

    def test_reload_success_and_health_check_running(self, tmp_path) -> None:
        """Full reload flow: reload OK, health check says 'running' → bak removed."""
        config_file = tmp_path / "syslog.conf"
        bak_file = tmp_path / "syslog.conf.bak"
        config_file.write_text("content")
        bak_file.write_text("backup")

        run_count = 0

        def fake_run_ctl(cmd, log_errors=True):
            nonlocal run_count
            run_count += 1
            if run_count == 1:
                return "Reloaded"
            return "syslog-ng is running"

        with (
            patch(
                "netbox2prom.generators.syslog.shutil.which", return_value="/usr/sbin/syslog-ng-ctl"
            ),
            patch("netbox2prom.generators.syslog.time.sleep"),
            patch("netbox2prom.generators.syslog._run_ctl", side_effect=fake_run_ctl),
            patch("netbox2prom.generators.syslog._remove_safely") as mock_remove,
        ):
            reload_syslog({"config_file": str(config_file), "health_check_delay": 0})
            mock_remove.assert_called_once_with(str(bak_file))

    def test_reload_health_check_not_running_triggers_rollback(self, tmp_path) -> None:
        """When syslog-ng is not running after reload, rollback is attempted."""
        config_file = tmp_path / "syslog.conf"
        bak_file = tmp_path / "syslog.conf.bak"
        config_file.write_text("new")
        bak_file.write_text("old")

        run_count = 0

        def fake_run_ctl(cmd, log_errors=True):
            nonlocal run_count
            run_count += 1
            if run_count == 1:
                return "ok"
            return "stopped"  # not running

        with (
            patch(
                "netbox2prom.generators.syslog.shutil.which", return_value="/usr/sbin/syslog-ng-ctl"
            ),
            patch("netbox2prom.generators.syslog.time.sleep"),
            patch("netbox2prom.generators.syslog._run_ctl", side_effect=fake_run_ctl),
            patch("netbox2prom.generators.syslog._rollback") as mock_rollback,
        ):
            reload_syslog({"config_file": str(config_file), "health_check_delay": 0})
            mock_rollback.assert_called_once()

    def test_reload_cmd_fails_skips_health_check(self, tmp_path) -> None:
        config_file = tmp_path / "syslog.conf"

        with (
            patch(
                "netbox2prom.generators.syslog.shutil.which", return_value="/usr/sbin/syslog-ng-ctl"
            ),
            patch("netbox2prom.generators.syslog._run_ctl", return_value=None),
            patch("netbox2prom.generators.syslog.time.sleep") as mock_sleep,
        ):
            reload_syslog({"config_file": str(config_file)})
            mock_sleep.assert_not_called()


