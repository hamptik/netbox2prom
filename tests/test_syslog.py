class TestValidateStructure:
    def test_valid_block(self) -> None:
        content = '# comment\nrewrite fix_hostnames {\n  set("rtr1" value(".Host"));\n};\n'
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is True
        assert error == ""

    def test_missing_opening(self) -> None:
        valid, error = _validate_structure("some text\n};\n", "fix_hostnames")
        assert valid is False
        assert "Expected block to start" in error

    def test_missing_closing(self) -> None:
        valid, error = _validate_structure("rewrite fix_hostnames {\n", "fix_hostnames")
        assert valid is False
        assert "end with" in error

    def test_unbalanced_braces(self) -> None:
        content = "rewrite fix_hostnames {\n{\n};\n"
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is False
        assert "Unbalanced" in error

    def test_unmatched_closing_brace(self) -> None:
        content = "rewrite fix_hostnames {\n}\n}\n};\n"
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is False
        assert "Unmatched closing brace" in error

    def test_braces_inside_strings_ignored(self) -> None:
        content = 'rewrite fix_hostnames {\n  set("hello}world" value(".Test"));\n};\n'
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is True

    def test_escaped_quotes_in_strings(self) -> None:
        content = 'rewrite fix_hostnames {\n  set("say \\"hello\\"" value(".Test"));\n};\n'
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is True

    def test_comments_skipped(self) -> None:
        content = "# comment with } brace\nrewrite fix_hostnames {\n  rule;\n};\n"
        valid, error = _validate_structure(content, "fix_hostnames")
        assert valid is True


